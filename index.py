print("Phầm mềm tính điểm trung bình")
# nhập điểm toán 
toan = int(input("Nhập Điểm Toán : "))
# nhập Điểm văn 
van = int(input("Nhập Điểm Văn : "))
# nhập điểm tiếng anh 
anh = int(input("Nhập Điểm Tiếng Anh : "))
# tính tông điểm 3 môn 
tong = toan + van + anh 
# tính trung bình cộng
tbc = tong / 3 
# in ra kết quả 
print("Điểm Trung Bình là : " ,tbc)
